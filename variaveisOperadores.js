resultado = num_A * num_B;
console.log(`Resultado da multiplicação entre ${num_A} e ${num_B} é ${resultado}`);

resultado = num_A / num_B;
console.log(`Resultado da divisão entre ${num_A} e ${num_B} é ${resultado}`);
